function PoolUniqueName(addr0: string, addr1: string): string {
  const sortedTokens = [addr0, addr1].sort();
  return sortedTokens.join("-");
}

function TokenPermutation(): Array<string> {
  let pairs = [];
  for (let i = 0; i < total_tokens - 1; i++) {
    for (let j = i + 1; j < total_tokens; j++) {
      const pair = [token_addresses[i], token_addresses[j]];
      pairs.push(PoolUniqueName(pair[0], pair[1]));
    }
  }

  return pairs;
}

function GetTokensByUniqueName(name: string): Array<string> {
  return name.split("-")
}
